    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        String nombreAlumno = campoNombre.getText();
        String[] codigoRamo = campoCodigo.getSelectedItem().toString().split(" - ");
        String estadoRamoTexto = campoCodigo.getSelectedItem().toString();
        int estadoRamo;
        Aviso avisoEmergente = new Aviso();
        
        if(estadoRamoTexto.equals("No cursado")){
            estadoRamo = 0;
        }else if(estadoRamoTexto.equals("Cursando")){
            estadoRamo = 1;
        }else{
            estadoRamo = 2;
        }
        
        if(inst.actualizarRamo(nombreAlumno, codigoRamo[1], estadoRamo)){
            avisoEmergente.cambiarAviso("Se ha podido actualizar el estado del ramo");
        }else{
            avisoEmergente.cambiarAviso("No se ha podido actualizar el estado del ramo");
        }
        
        avisoEmergente.setVisible(true);
        campoNombre.setText("");
        campoCodigo.removeAllItems();
        campoEstado.setSelectedIndex(0);
        jPanel1.setVisible(false);
    }//GEN-LAST:event_botonEditarActionPerformed


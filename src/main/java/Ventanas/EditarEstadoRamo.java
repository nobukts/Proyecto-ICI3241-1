    private void actualizarCampoCodigo(String nombreAlumno){
        campoCodigo.removeAllItems();
        campoCodigo.addItem("");
        String[] listaRamos = inst.buscarRamosAlumno(nombreAlumno);
        
        for (int i = 0; i < listaRamos.length; i++) {
            String[] infoRamo = listaRamos[i].split("-");
            String infoColumna = infoRamo[0] + " - " + infoRamo[1];
            campoCodigo.addItem(infoColumna);
        }
    }
    
